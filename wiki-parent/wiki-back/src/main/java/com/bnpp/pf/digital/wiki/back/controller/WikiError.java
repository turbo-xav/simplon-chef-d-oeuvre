package com.bnpp.pf.digital.wiki.back.controller;

public class WikiError {

	private String msg = "";

	public WikiError(String msg) {
		super();
		this.msg = msg;
	}

	/**
	 * @return the msg
	 */
	public String getMsg() {
		return msg;
	}

	/**
	 * @param msg the msg to set
	 */
	public void setMsg(String msg) {
		this.msg = msg;
	}
}
